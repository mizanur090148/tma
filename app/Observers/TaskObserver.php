    /**
     * Handle the task "created" event.
     *
     * @param  \App\Task  $task
     * @return void
     */
    public function created(Task $task)
    {
        if (!$task->parent_id) {
            $task->update(['master_parent_id' => $task->id]);           
        } else {
            $taskNew = Task::find($task->parent_id);
            $task->update(['master_parent_id' => $taskNew->master_parent_id]);
        }
    }

